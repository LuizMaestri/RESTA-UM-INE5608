package br.com.ufsc.aps.restaum.model;

public class Estatisticas {

	private Integer qntJogadas;
	private Integer pecasComidas;
	private Double tempoJogador;
	
	public Integer getQntJogadas() {
		return qntJogadas;
	}
	public void setQntJogadas(Integer qntJogadas) {
		this.qntJogadas = qntJogadas;
	}
	public Integer getPecasComidas() {
		return pecasComidas;
	}
	public void setPecasComidas(Integer pecasComidas) {
		this.pecasComidas = pecasComidas;
	}
	public Double getTempoJogador() {
		return tempoJogador;
	}
	public void setTempoJogador(Double tempoJogador) {
		this.tempoJogador = tempoJogador;
	}
	
	
}
